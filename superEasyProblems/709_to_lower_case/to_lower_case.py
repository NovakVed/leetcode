# 709 To Lower Case
# https://leetcode.com/problems/to-lower-case/description/
# Explanation: https://leetcode.com/problems/to-lower-case/?envType=problem-list-v2&envId=prshgx6i
class Solution:
    def toLowerCase(self, s: str) -> str:
        return s.lower()

s = Solution()
print(s.toLowerCase('VEKI'))